function updateCartFlexboxClass() {
  fetch("/cart.js")
    .then((response) => response.json())
    .then((cart) => {
      const cartFlexbox = document.querySelector(".cart-flexbox");
      if (cartFlexbox) {
        cartFlexbox.classList.toggle("empty-cart", cart.item_count === 0);
      }
    });
}

async function customiserUpdateHandle(line, quantity, variantId) {
  let updates = {};
  let hasChildren = false;
  let fetchRoute = routes.cart_update_url;

  try {
      const response = await fetch(`${routes.cart_url}.js`);
      const cart = await response.json();
      const items = cart.items;

      const parentItem = items[line - 1];
      if (!parentItem) {
          return { fetchRoute, updates };
      }

      if (!parentItem?.properties?._customer_product_id && !parentItem?.properties?._gpo_product_group) {
          return { fetchRoute, updates };
      }

      const parentProductId = parentItem.properties._customer_product_id || parentItem.properties._gpo_product_group;
      updates[parentItem.key] = quantity;

      const parentExtrasRaw = parentItem.properties?.Extras || '';
      const extrasArray = parentExtrasRaw.split(',').map(e => e.trim());

      const extrasCountMap = {};
      for (const name of extrasArray) {
        if (!name) continue;
        extrasCountMap[name] = (extrasCountMap[name] || 0) + 1;
      }

      items.forEach(child => {
          const childProductId = child.properties?._parent_product_id || child.properties._gpo_parent_product_group
          if (childProductId === parentProductId) {
            const childExtraName = child.variant_title.trim();
            const flatQty = extrasCountMap[childExtraName];
            
            if (flatQty !== undefined) {
              updates[child.key] = flatQty * quantity;
              hasChildren = true;
            }
            else {
              updates[child.key] = quantity;
            }
          }
      });

      if (hasChildren) {
        fetchRoute = routes.cart_update_url;
      }

  } catch (error) {
      console.error('Error fetching cart:', error);
      return null;
  }

  return { fetchRoute, updates };
}

